-- --------------------------------------------------------
-- Table: users
-- --------------------------------------------------------
CREATE TABLE users (
    id         NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name       VARCHAR2(100) NOT NULL,
    email      VARCHAR2(150) NOT NULL UNIQUE,
    password   VARCHAR2(255) NOT NULL,
    role       VARCHAR2(20) DEFAULT 'employee' NOT NULL CHECK (role IN ('employee','manager')),
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- --------------------------------------------------------
-- Table: goals
-- --------------------------------------------------------
CREATE TABLE goals (
    goal_id     NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id     NUMBER NOT NULL,
    title       VARCHAR2(200) NOT NULL,
    description CLOB,
    deadline    DATE,
    status      VARCHAR2(20) DEFAULT 'pending' NOT NULL CHECK (status IN ('pending','in_progress','completed')),
    priority    VARCHAR2(20) DEFAULT 'medium' NOT NULL CHECK (priority IN ('low','medium','high')),
    created_at  TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_goal_user FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE CASCADE
);

-- --------------------------------------------------------
-- Table: assignments
-- --------------------------------------------------------
CREATE TABLE assignments (
    assignment_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    manager_id    NUMBER NOT NULL,
    employee_id   NUMBER NOT NULL,
    task          CLOB NOT NULL,
    created_at    TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_assign_manager FOREIGN KEY (manager_id) REFERENCES users(id) ON DELETE CASCADE,
    CONSTRAINT fk_assign_employee FOREIGN KEY (employee_id) REFERENCES users(id) ON DELETE CASCADE
);

-- --------------------------------------------------------
-- Table: progress
-- --------------------------------------------------------
CREATE TABLE progress (
    progress_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    goal_id     NUMBER NOT NULL,
    status      VARCHAR2(20) DEFAULT 'pending' NOT NULL CHECK (status IN ('pending','in_progress','completed')),
    updated_at  TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_prog_goal FOREIGN KEY (goal_id) REFERENCES goals(goal_id) ON DELETE CASCADE
);

-- Trigger to update 'updated_at' on progress table automatically
CREATE OR REPLACE TRIGGER trg_progress_updated
BEFORE UPDATE ON progress
FOR EACH ROW
BEGIN
   :NEW.updated_at := CURRENT_TIMESTAMP;
END;
/

