    milestone_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    goal_id NUMBER REFERENCES goals(goal_id) ON DELETE CASCADE,
    description VARCHAR2(255) NOT NULL,
    is_completed NUMBER(1) DEFAULT 0 -- 0: Pending, 1: Completed
